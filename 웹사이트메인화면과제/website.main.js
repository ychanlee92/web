function loginWindow() {
  let popupWidth = 600;
  let popupHeight = 600;
  let popupX = window.screen.width / 2 - popupWidth / 2;
  let popupY = window.screen.height / 2 - popupHeight / 1.5;
  window.open(
    "",
    "",
    "status=no, height=" +
      popupHeight +
      ", width=" +
      popupWidth +
      ", left=" +
      popupX +
      ", top=" +
      popupY
  );
}
